/*** Main parser section ***/

type gen struct {
	g  *ast.GoGLL
	gs *gslot.GSlot
	ff *frstflw.FF
}

func Gen(g *ast.GoGLL, gs *gslot.GSlot, ff *frstflw.FF) {
	parserDir := path.Join(cfg.BaseDir, "parser")
	gn := &gen{g, gs, ff}
	gn.genParser(parserDir)
	bsr.Gen(filepath.Join(parserDir, "bsr", "bsr.go"), g.Package.GetString())
	slots.Gen(filepath.Join(parserDir, "slot", "slot.go"), g, gs, ff)
	symbols.Gen(filepath.Join(parserDir, "symbols", "symbols.go"), g, ff)
}

func (g *gen) genParser(parserDir string) {
	buf := new(bytes.Buffer)
	tmpl, err := template.New("Parser Main Template").Parse(mainTemplate)
	if err != nil {
		parseErrorError(err)
	}
	data := g.getData(parserDir)
	if err = tmpl.Execute(buf, data); err != nil {
		parseErrorError(err)
	}
	fmtSrc, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Printf("Error formatting generated parsers: %s\n", err)
		fmtSrc = buf.Bytes()
	}
	fname := path.Join(parserDir, "parser.go")
	if err := ioutil.WriteFile(fname, []byte(fmtSrc)); err != nil {
		parseErrorError(err)
	}
}

type Data struct {
	Package     string
	StartSymbol string
	ExtraLabels []string
	LastSlot    int
	CodeX       string
	TestSelect  string
}

func (g *gen) getData(baseDir string) *Data {
	extraLabels := make([]string, 0, g.g.NonTerminals.Len())
	for _, nt := range g.g.SyntaxRules {
		if !nt.AlwaysMatches() {
			extraLabels = append(extraLabels, `fail_`+nt.ID())
			if !nt.IsOrdered {
				for i, alt := range nt.Alternates {
					if i == 0 {
						continue
					}
					for j, sym := range alt.Symbols {
						if j == 0 {
							extraLabels = append(extraLabels, `pass_`+gslot.LabelFor(nt.ID(), i, j))
						}
						if _, ok := sym.(*ast.NT); ok {
							extraLabels = append(extraLabels, `pass_`+gslot.LabelFor(nt.ID(), i, j+1))
						}
					}
				}
			}
		}
	}
	data := &Data{
		Package:     g.g.Package.GetString(),
		StartSymbol: g.g.StartSymbol(),
		ExtraLabels: extraLabels,
		LastSlot:    g.gs.Len() - 1,
		CodeX:       g.genAlternatesCode(),
		TestSelect:  g.genTestSelect(),
	}
	return data
}

